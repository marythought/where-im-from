var exports = module.exports = {};

exports.runPoem = function(){
  Poem = "Two roads diverged in a yellow wood,\n"
  Poem += "And sorry I could not travel both\n"
  Poem += "And be one traveler, long I stood\n"
  Poem += "And looked down one as far as I could\n"
  Poem += "To where it bent in the undergrowth;\n"
  Poem += "\n"
  Poem += "Then took the other, as just as fair,\n"
  Poem += "And having perhaps the better claim\n"
  Poem += "Because it was grassy and wanted wear,\n"
  Poem += "Though as for that the passing there\n"
  Poem += "Had worn them really about the same,\n"
  Poem += "\n"
  Poem += "And both that morning equally lay\n"
  Poem += "In leaves no step had trodden black.\n"
  Poem += "Oh, I kept the first for another day!\n"
  Poem += "Yet knowing how way leads on to way\n"
  Poem += "I doubted if I should ever come back.\n"
  Poem += "\n"
  Poem += "I shall be telling this with a sigh\n"
  Poem += "Somewhere ages and ages hence:\n"
  Poem += "Two roads diverged in a wood, and I,\n"
  Poem += "I took the one less traveled by,\n"
  Poem += "And that has made all the difference."
  return Poem
};
